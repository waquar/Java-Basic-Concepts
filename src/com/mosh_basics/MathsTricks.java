package com.mosh_basics;

public class MathsTricks {
    public static void main(String[] args) {
        int result = Math.round(1.6F);
        System.out.println(result);
        int result1 = Math.max(500, 433);
        System.out.println(result1);
        double result2 = Math.random();   //Math.random returns double
        System.out.println(result2);
        double result3= Math.random()*350;   //Math.random returns double till value 350
        System.out.println(result3);



    }
}