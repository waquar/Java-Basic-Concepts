package com.harry;

/*
    for(statement1 ; statement 2 ; statement3){
    code to be executed
    }
            statement1 : runs before the forloop starts once.
            statement2 : condition to check. if true then only code runs.
            statement3 : runs after every loop iteration.
 */

public class forloop {
    public static void main(String[] args){
        for(int i =0; i<10; i++){
            System.out.println(i);
        }
    }
}
